import datetime

def get_study_week():
    # Дата початку навчання: 16 лютого 2026 року
    start_date = datetime.date(2026, 2, 16)
    today = datetime.date.today()

    # Рахуємо, скільки повних тижнів минуло
    days_diff = (today - start_date).days
    week_num = (days_diff // 7) + 1

    # Визначаємо: 1-й (непарний) або 2-й (парний)
    return "1-й" if week_num % 2 != 0 else "2-й"

